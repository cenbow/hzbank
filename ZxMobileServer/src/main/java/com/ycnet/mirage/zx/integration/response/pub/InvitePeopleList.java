package com.ycnet.mirage.zx.integration.response.pub;

public class InvitePeopleList {
	
	private String customerNameCN;

	private String customerAlias;

	private String mobileNo;

	private String email;

	private String createTime;
	
	private String photo;
	

	public String getCustomerNameCN() {
		return customerNameCN;
	}

	public void setCustomerNameCN(String customerNameCN) {
		this.customerNameCN = customerNameCN;
	}

	public String getCustomerAlias() {
		return customerAlias;
	}

	public void setCustomerAlias(String customerAlias) {
		this.customerAlias = customerAlias;
	}

	public String getMobileNo() {
		return mobileNo;
	}

	public void setMobileNo(String mobileNo) {
		this.mobileNo = mobileNo;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getCreateTime() {
		return createTime;
	}

	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}
	
	public String getPhoto() {
		return photo;
	}

	public void setPhoto(String photo) {
		this.photo = photo;
	}

}
